package core.veritification.concretes;

public class Veritification {
private int veritificationId;
private String veritificationName;

public int getVeritificationId() {
	return veritificationId;
}
public void setVeritificationId(int veritificationId) {
	this.veritificationId = veritificationId;
}
public String getVeritificationName() {
	return veritificationName;
}
public void setVeritificationName(String veritificationName) {
	this.veritificationName = veritificationName;
}

}
